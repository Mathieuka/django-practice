from tutorial.snippets.models import Snippet
from tutorial.snippets.serializers import SnippetSerializer
from rest_framework import mixins
from rest_framework import generics
from rest_framework import serializers
from dataclasses import dataclass

@dataclass
class SnippetParams:
    pk: int

class UrlParamsSerializer(serializers.Serializer):
    pk = serializers.IntegerField(min_value=1)

    def validate_pk(self, value):
        # E.G business rull
        if value == 999:
            raise serializers.ValidationError("Valeur réservée")
        return value

class SnippetList(mixins.ListModelMixin,
                  mixins.CreateModelMixin,
                  generics.GenericAPIView):
    queryset = Snippet.objects.all()
    serializer_class = SnippetSerializer

    def get(self, request, *args, **kwargs):
        return self.list(request, *args, **kwargs)

    def post(self, request, *args, **kwargs):
        return self.create(request, *args, **kwargs)

class SnippetDetail(mixins.RetrieveModelMixin,
                    mixins.UpdateModelMixin,
                    mixins.DestroyModelMixin,
                    generics.GenericAPIView):
    queryset = Snippet.objects.all()
    serializer_class = SnippetSerializer

    def get(self, request, *args, **kwargs: SnippetParams):

        # Validate with serializer
        params_serializer = UrlParamsSerializer(data=kwargs)
        params_serializer.is_valid(raise_exception=True)

        # Typing for autocompletion
        validated_params = SnippetParams(**params_serializer.validated_data)
        print("validated_params ", validated_params)
        return self.retrieve(request, *args, **kwargs)

    def put(self, request, *args, **kwargs):
        return self.update(request, *args, **kwargs)

    def delete(self, request, *args, **kwargs):
        return self.destroy(request, *args, **kwargs)

